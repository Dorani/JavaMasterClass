package Expressions_Statements_Methods;

public class Statements {
    public static void main(String[] args) {
        //Examples:
        int myVar = 50;
        myVar++;
        myVar--;
        System.out.println(myVar + "this is also a statement");
        System.out.println("This is also a statement, like the above code");
        //another way to add more to our statement
        System.out.println("This is" +
                " another" +
                " still more");
    }
}
